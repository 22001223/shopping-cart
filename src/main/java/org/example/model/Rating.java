package org.example.model;

public class Rating {
    private Double rate;
    private Integer count;

    // Getters and setters
    public Double getRate() {
        return rate;
    }

    public void setRate(Double rate) {
        this.rate = rate;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }
}